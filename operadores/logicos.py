# # OPERADORES LOGICOS
# SI LOS DOS BOOLENAOS SON TRUE ES = (True), SI HAY ALGUN FALSE ES = (False)
print(True and True)
print(True and False)
print(False and True)
print(False and False)

# SI HAY 1 TRUE DENTRO DE LOS BOOLEANOS ES = (True), SI TODOS SON FALSE = (False)
print(True or True)
print(False or True)
print(True or False)
print(False or False)


